#Operators in python
#Definition: 
# An operator tells a computer what to do with an operand.
# An operand is what an operator acts upon.(value)
#These operators are categorized;
#Arithmetic operators(+,-,/,//,*,%,**)
#EXxamples
num1 = 10
num2 = 20
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 ** num2)
print(num1 % num2)
print(num1 / num2)
print(num1 // num2)


#Assignment operators(= {assignment},+={adding two vales},-=,*=,/=,%=,**=)
num3 = 50
num4 = 100
num3 += num4  #(its the same as num3 = num3 + num4)
print(num3)
num3 -= num4
print(num3)
num4 += num3
print(num4)
# comparison operators(==,!=,<,>,>=,<=)
print(num1 == num2)
print(num1 != num2)
print(num1 < num2)
print(num1 <= num2)
# logical operators(and,or,not,)
print(True and True)
print( True and False)
print(True or False)
print(not True)
#identity operators(is,is not)
name = "Vanessa"
print(num3 is num4)
print(num3 is not num4)
# membership operators(in, not in)
print("a" in name)
print("o" in name)
print("A" in name)
print("o" not in name)
    



