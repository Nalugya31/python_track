'''
len is a method which is polymorphic.
len means helps us to know the length of strings, the number of words or elements in a list
and the number of keys in a dictionary.


'''

print(len("Programming "+"is fun!"))# number of characters
# the + is polymorphic too(it concatenates strings and then adds integers.)
print("Programming "+"is fun!") 
print(len(['python','java','js','nodejs']))#number of elements in the list
print(len({"name":"vanessa","country": "rwanda", "city": "kigali"}))# number of keys in the dictionary.

      
